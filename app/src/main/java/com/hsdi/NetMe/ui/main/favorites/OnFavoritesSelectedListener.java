package com.hsdi.NetMe.ui.main.favorites;

import com.hsdi.NetMe.models.Contact;

interface OnFavoritesSelectedListener {

    /**
     * Call for when a contact in the favorite fragment has been normally clicked
     * @param contact    the contact which was clicked
     * */
    void onSelected(Contact contact);

    /**
     * Call for when a contact in the favorite fragment has been long clicked
     * @param contact    the contact which was clicked
     * */
    void onLongSelected(Contact contact);
}
